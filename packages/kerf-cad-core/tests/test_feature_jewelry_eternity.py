# ---------------------------------------------------------------------------
# Cross-size system: same physical ring in US vs UK vs EU vs JP
# gives consistent stone counts (within ±2 due to rounding in size tables)
# ---------------------------------------------------------------------------

def test_size_system_consistency():
    """US 7, UK N, EU 54 are all ~17.1 mm ID; stone counts should agree ±2."""
    base = dict(stone_cut="round_brilliant", stone_mm=2.0,
                setting_style="prong", calibration_mode="fixed_size",
                coverage="full")
    us = eternity_auto_distribute(ring_size=7,    size_system="us", **base)
    uk = eternity_auto_distribute(ring_size="N",  size_system="uk", **base)
    eu = eternity_auto_distribute(ring_size=54,   size_system="eu", **base)
    counts = [us["stone_count"], uk["stone_count"], eu["stone_count"]]
    assert max(counts) - min(counts) <= 2, (
        f"Cross-system stone counts diverge too much: us={counts[0]}, "
        f"uk={counts[1]}, eu={counts[2]}"
    )

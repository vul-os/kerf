def test_part_filename_sanitises_jewelry_name():
    """part_filename() produces a safe .part filename for a jewelry part name."""
    fn = part_filename("Jewelry plain round bangle XL")
    assert fn.endswith(".part")
    # No shell-unsafe characters.
    for ch in ("/", "\\", " ", "&", "|"):
        assert ch not in fn, f"unsafe char {ch!r} in filename {fn!r}"



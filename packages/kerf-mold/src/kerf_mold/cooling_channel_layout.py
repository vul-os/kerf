"""
kerf_mold.cooling_channel_layout — 3D cooling channel routing layout design.

Theory & References
-------------------
Menges G., Michaeli W., Mohren P. "How to Make Injection Molds", 3rd ed.,
  Hanser 2001 — §6.5 Cooling-channel design rules:
    Channel bore diameter: typically 8–14 mm for standard cavity blocks.
    Centre-to-centre pitch: 3–5× bore diameter.
    Distance from cavity surface: 1.5–2.5× bore diameter.
    Series vs. parallel circuit layout trade-offs.

Rosato D.V., Rosato M.G. "Injection Molding Handbook", 3rd ed.,
  Kluwer Academic 2000 — §8 Cooling system design.

Tang L.Q., Chassapis C., Manoochehri S. (1998). Optimal cooling
  system design for multi-cavity injection moulding.
  *Finite Elements in Analysis and Design*, 26(3), 229–251.
  — Covers straight gun-drilled channels and baffle/bubbler configurations.

Xu X., Sachs E., Allen S. (2001). The design of conformal cooling
  channels in injection molding tooling.
  *Polymer Engineering & Science*, 41(7), 1265–1279.
  — Conformal cooling channel offset distance and coverage rules.

Capability scope
----------------
This module designs the **routing geometry** of cooling channels — the 3-D
positions, orientations, diameter, and pitch of each drilled (or conformal)
channel run — as a complement to the thermal analysis already implemented in
kerf_mold.cooling (Dittus-Boelter HTC, pressure drop, cooling time).

Two layout strategies are provided:

  conventional:
    Straight gun-drilled channels arranged in a grid parallel to the parting
    plane.  Industry-standard approach for CNC-machined molds.  Channels are
    specified by (start, end) 3-D line segments with uniform bore diameter
    and pitch.  Bubblers and baffles can be added for deep core regions.

  conformal:
    Channels that follow the cavity-surface offset at a constant distance
    (Xu et al. 2001 rule: offset = 1.5–2× bore diameter from cavity surface).
    Represented as a polyline following the outer contour of the cavity bbox,
    suitable for additive-manufactured (3D-printed) mold inserts.

Output format
-------------
Each channel is a CoolingChannelRoute dataclass with:
  start_mm, end_mm  : 3-D endpoints (mm)
  diameter_mm       : bore diameter
  label             : human-readable identifier
  channel_type      : 'straight' | 'baffle' | 'bubbler' | 'conformal'

A CoolingLayoutReport contains all channels, summary statistics, and
honest caveats about the limitations of the heuristic placement rules.

HONEST CAVEAT
-------------
Channel positions are generated by heuristic rules from Menges 2001 §6.5 and
Xu et al. 2001.  Real cooling-channel layouts must be verified for:
  (a) mechanical interference with cavity features, ejector pins, and other
      inserts (use kerf_mold.cooling_channel_conflict for interference checks);
  (b) thermal uniformity (use kerf_mold.cooling for HTC/pressure-drop analysis);
  (c) manufactureability (gun-drilled holes need entry/exit faces; conformal
      channels need AM or electrode-discharge tooling).
This module does NOT perform thermal FEM simulation.  Use Moldflow / Moldex3D
for production-quality cooling analysis.

Wave 9C: Cimatron mold base + EDM electrode + wire EDM + cooling channel layout
"""
from __future__ import annotations

import math
from dataclasses import dataclass, field
from typing import List, Optional, Tuple



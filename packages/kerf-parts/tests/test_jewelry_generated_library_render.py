@pytest.mark.parametrize("size", ["S", "M", "L", "XL"])
def test_generated_bangle_doc_roundtrips_json(size):
    """to_part_doc() serialises to JSON without error; key fields survive the trip."""
    part = _make_generated_bangle_part(size)
    doc = part.to_part_doc()
    serialised = json.dumps(doc)
    restored = json.loads(serialised)
    assert restored["category"] == doc["category"]
    assert restored["name"] == doc["name"]
    assert restored["version"] == 1


# ===========================================================================
# Content hash (incremental seed)
# ===========================================================================


@pytest.mark.parametrize("size", ["S", "M", "L", "XL"])
def test_generated_bangle_content_hash_deterministic(size):
    """content_hash is stable across two ensure_hash() calls (no timestamp)."""
    p1 = _make_generated_bangle_part(size)
    p2 = _make_generated_bangle_part(size)
    h1 = p1.ensure_hash()
    h2 = p2.ensure_hash()
    assert h1 == h2, f"size={size}: content_hash unstable ({h1} vs {h2})"


def test_generated_bangle_hash_differs_across_sizes():
    """Content hashes are distinct for distinct sizes (no accidental collision)."""
    hashes = {
        size: _make_generated_bangle_part(size).ensure_hash()
        for size in ("S", "M", "L", "XL")
    }
    assert len(set(hashes.values())) == 4, (
        f"hash collision across sizes: {hashes}"
    )



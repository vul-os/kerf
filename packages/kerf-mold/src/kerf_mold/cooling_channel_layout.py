# Design constants (Menges 2001 §6.5)
# ---------------------------------------------------------------------------

# Recommended bore diameter range (mm)
BORE_DIAMETER_MIN_MM: float = 6.0
BORE_DIAMETER_MAX_MM: float = 16.0
BORE_DIAMETER_DEFAULT_MM: float = 10.0

# Pitch multiplier: pitch = pitch_factor × diameter (Menges 2001 §6.5 Table 6.4)
# Min 2.5×D for aggressive cooling; 3–4×D standard; 5×D conservative
PITCH_FACTOR_MIN: float = 2.5
PITCH_FACTOR_STANDARD: float = 3.5
PITCH_FACTOR_MAX: float = 5.0

# Cavity wall clearance: distance from cavity surface = clearance_factor × diameter
# Menges 2001 §6.5: minimum 1.5×D; standard 2×D; maximum 3×D
WALL_CLEARANCE_FACTOR_MIN: float = 1.5
WALL_CLEARANCE_FACTOR_STANDARD: float = 2.0
WALL_CLEARANCE_FACTOR_MAX: float = 3.0

# Conformal channel offset from cavity surface (Xu et al. 2001 §3)
CONFORMAL_OFFSET_FACTOR: float = 1.8  # offset = 1.8 × diameter



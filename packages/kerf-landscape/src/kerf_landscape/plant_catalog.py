def plants_for_pollinators(
    region: Literal["east", "west", "south", "north"],
) -> list[PlantSpecies]:
    """
    Return pollinator-friendly species for the given region.

    Parameters
    ----------
    region : 'east' | 'west' | 'south' | 'north'
        Broad US region.

    Returns
    -------
    list[PlantSpecies]
        Species recommended for pollinators in the region.
        Returns empty list for unknown region.
    """
    sci_names = _POLLINATOR_SUBSETS.get(region, [])
    result: list[PlantSpecies] = []
    for name in sci_names:
        sp = PLANT_CATALOG.get(name)
        if sp is not None:
            result.append(sp)
    return result

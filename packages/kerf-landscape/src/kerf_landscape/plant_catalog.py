"""
Native plant species catalog for Kerf landscape design.

Data sources
------------
* Dirr, Michael A. *Manual of Woody Landscape Plants*, 6th ed., Stipes (2009).
  Canonical reference for mature dimensions, zone ranges, and growth rates.
* USDA Plant Hardiness Zone Map (2023 edition) — zone boundaries.
* Dirr & Heuser, *The Reference Manual of Woody Plant Propagation* (2006).
* Lady Bird Johnson Wildflower Center — native plant data.
* Missouri Botanical Garden Plant Finder — supplementary species data.

DISCLAIMER
----------
Species attributes are derived from published reference data (Dirr 2009,
USDA PHZM 2023, Missouri Botanical Garden). This catalog is for landscape
design assistance only and is **NOT USDA certified**. Verify zone suitability
and cultural requirements with local Extension Service offices before
installation.

Public API
----------
lookup_plant(name: str) -> PlantSpecies
    Case-insensitive lookup by common or scientific name.

filter_plants(usda_zone, light, water, kind=None, deer_resistant=None)
    -> list[PlantSpecies]
    Return plants suitable for the given conditions.

plants_for_pollinators(region: 'east'|'west'|'south'|'north')
    -> list[PlantSpecies]
    Pollinator-friendly species per region.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Literal


# ---------------------------------------------------------------------------
# PlantSpecies dataclass
# ---------------------------------------------------------------------------

KindLiteral = Literal[
    "deciduous_tree", "evergreen", "shrub", "perennial", "grass", "groundcover"
]
LightLiteral = Literal["full_sun", "partial_shade", "shade"]
WaterLiteral = Literal["low", "medium", "high"]


@dataclass(frozen=True)
class PlantSpecies:
    """
    Landscape plant with full design-specification attributes.

    Attributes
    ----------
    scientific_name : str
        Binomial (Genus species) following ICN nomenclature.
    common_name : str
        Most widely used common name in North American landscape trade.
    kind : KindLiteral
        Plant category: deciduous_tree / evergreen / shrub / perennial / grass / groundcover.
    mature_height_m : float
        Typical mature height in metres (Dirr midpoint or range mid).
    mature_spread_m : float
        Typical mature canopy spread in metres.
    growth_rate_cm_per_year : float
        Average annual growth rate in cm (slow <30, medium 30-60, fast >60).
    usda_zones_min : int
        Minimum USDA hardiness zone (cold-hardiness limit).
    usda_zones_max : int
        Maximum USDA hardiness zone (heat-tolerance limit).
    light : LightLiteral
        Primary light requirement: full_sun / partial_shade / shade.
    water : WaterLiteral
        Water demand once established: low / medium / high.
    soil_type : str
        Preferred soil texture / drainage (free text, Dirr notes).
    bloom_color : str
        Dominant bloom color, or "none" for non-ornamental / foliage plants.
    deer_resistant : bool
        True when documented as generally deer-resistant in literature.
    notes : str
        Brief landscape design note (Dirr character / use).
    regions : tuple[str, ...]
        Geographic regions where naturally native or well-adapted:
        'east', 'west', 'south', 'north', 'midwest', 'mountain', 'pacific'.
    pollinator_value : str
        Documented pollinator benefit: 'high' / 'medium' / 'low' / 'none'.
        Includes bees, butterflies, hummingbirds, moths.
    """

    scientific_name: str
    common_name: str
    kind: str
    mature_height_m: float
    mature_spread_m: float
    growth_rate_cm_per_year: float
    usda_zones_min: int
    usda_zones_max: int
    light: str
    water: str
    soil_type: str
    bloom_color: str
    deer_resistant: bool
    notes: str = ""
    regions: tuple = ()
    pollinator_value: str = "low"



# Common-name → scientific name index (normalised lowercase)
_COMMON_INDEX: dict[str, str] = {
    sp.common_name.lower(): sci
    for sci, sp in PLANT_CATALOG.items()
}


def lookup_plant(name: str) -> PlantSpecies | None:
    """
    Case-insensitive lookup by common name or scientific name.

    Parameters
    ----------
    name : str
        Common name (e.g. 'red maple') or scientific name (e.g. 'Acer rubrum').

    Returns
    -------
    PlantSpecies | None
        Matching species, or None if not found.
    """
    key = name.strip().lower()
    if key in PLANT_CATALOG:
        return PLANT_CATALOG[key]
    if key in _COMMON_INDEX:
        return PLANT_CATALOG[_COMMON_INDEX[key]]
    return None


def filter_plants(
    usda_zone: int | None = None,
    light: str | None = None,
    water: str | None = None,
    kind: str | None = None,
    deer_resistant: bool | None = None,
) -> list[PlantSpecies]:
    """
    Filter the catalog by site conditions.

    Parameters
    ----------
    usda_zone : int | None
        USDA hardiness zone (1–13). Returns species whose zone range includes it.
    light : str | None
        Light requirement: 'full_sun', 'partial_shade', 'shade'.
    water : str | None
        Water demand: 'low', 'medium', 'high'.
    kind : str | None
        Plant category: 'deciduous_tree', 'evergreen', 'shrub', 'perennial',
        'grass', 'groundcover'.
    deer_resistant : bool | None
        If True, return only deer-resistant species.

    Returns
    -------
    list[PlantSpecies]
        Species matching all supplied criteria (AND logic).
    """
    results: list[PlantSpecies] = []
    for sp in PLANT_CATALOG.values():
        if usda_zone is not None and not (sp.usda_zones_min <= usda_zone <= sp.usda_zones_max):
            continue
        if light is not None and sp.light != light:
            continue
        if water is not None and sp.water != water:
            continue
        if kind is not None and sp.kind != kind:
            continue
        if deer_resistant is not None and sp.deer_resistant != deer_resistant:
            continue
        results.append(sp)
    return results



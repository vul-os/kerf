# ---------------------------------------------------------------------------
# Boundary: half eternity stone count < three_quarter < full
# ---------------------------------------------------------------------------

def test_coverage_ordering():
    base = dict(ring_size=7, stone_cut="round_brilliant", stone_mm=2.0,
                setting_style="prong", calibration_mode="fixed_size")
    full = eternity_auto_distribute(**base, coverage="full")
    tq   = eternity_auto_distribute(**base, coverage="three_quarter")
    half = eternity_auto_distribute(**base, coverage="half")
    assert half["stone_count"] < tq["stone_count"] < full["stone_count"]



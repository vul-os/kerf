def design_cooling_channel_layout(
    block: MoldBlockSpec,
    cavity_depth_mm: float,
    layout_type: str = "conventional",
    bore_diameter_mm: Optional[float] = None,
    pitch_factor: float = PITCH_FACTOR_STANDARD,
    clearance_factor: float = WALL_CLEARANCE_FACTOR_STANDARD,
    num_circuits: int = 2,
    num_conformal_segments: int = 8,
) -> CoolingLayoutReport:
    """Design a 3D cooling channel layout for an injection-mold cavity block.

    Two strategies
    --------------
    'conventional':
      Straight gun-drilled channels arranged in a grid parallel to the
      parting plane.  Industry standard for machined molds.

    'conformal':
      Channel loop following the cavity-surface contour at a constant
      offset distance (Xu et al. 2001).  For additive-manufactured inserts.

    Parameters
    ----------
    block             : MoldBlockSpec — bounding box of the mold cavity block.
    cavity_depth_mm   : Depth of the deepest cavity feature (mm).
    layout_type       : 'conventional' | 'conformal'. Default 'conventional'.
    bore_diameter_mm  : Channel bore diameter (mm). If None, selected
                        automatically from block size (Menges 2001 §6.5 Table 6.4).
    pitch_factor      : Conventional layout only. Pitch = factor × diameter.
                        Typical: 3.5 (standard), 2.5 (aggressive), 5.0 (conservative).
    clearance_factor  : Distance from cavity surface = factor × bore diameter.
                        Menges 2001 §6.5 minimum 1.5, standard 2.0.
    num_circuits      : Number of independent cooling circuits (1 or 2).
    num_conformal_segments : Number of perimeter segments per conformal loop.
                            Minimum 4 (rectangular). Default 8.

    Returns
    -------
    CoolingLayoutReport

    References
    ----------
    Menges G., Michaeli W., Mohren P. "How to Make Injection Molds", 3rd ed.,
      Hanser 2001, §6.5 — cooling channel design rules; Table 6.4.
    Xu X., Sachs E., Allen S. (2001). *Polymer Engineering & Science* 41(7),
      1265–1279 — conformal cooling channel offset rules.
    Tang L.Q. et al. (1998). *Finite Elements in Analysis and Design* 26(3),
      229–251 — optimal cooling channel placement.

    HONEST CAVEAT
    -------------
    Positions are generated by heuristic rules.  Verify:
    (a) interference via kerf_mold.cooling_channel_conflict;
    (b) thermal performance via kerf_mold.cooling (Dittus-Boelter HTC);
    (c) manufacturability (gun-drilled access, AM print direction).
    This module does NOT simulate part cooling or temperature uniformity.
    Real cooling optimisation requires Moldflow / Moldex3D FEM simulation.
    """
    warnings: List[str] = []

    # Validate inputs
    if cavity_depth_mm <= 0:
        raise ValueError(f"cavity_depth_mm must be > 0, got {cavity_depth_mm}")
    valid_layouts = {"conventional", "conformal"}
    if layout_type not in valid_layouts:
        raise ValueError(
            f"layout_type must be one of {sorted(valid_layouts)}, got {layout_type!r}"
        )
    if num_circuits < 1:
        raise ValueError(f"num_circuits must be >= 1, got {num_circuits}")
    if clearance_factor < WALL_CLEARANCE_FACTOR_MIN:
        warnings.append(
            f"clearance_factor {clearance_factor:.2f} < minimum {WALL_CLEARANCE_FACTOR_MIN}. "
            f"Risk of cavity wall breakthrough. Menges 2001 §6.5 minimum: {WALL_CLEARANCE_FACTOR_MIN}×D."
        )
    if pitch_factor < PITCH_FACTOR_MIN:
        warnings.append(
            f"pitch_factor {pitch_factor:.2f} < minimum {PITCH_FACTOR_MIN}. "
            f"Channels may be too close together."
        )

    # Select bore diameter
    if bore_diameter_mm is None:
        bore_diameter_mm = _recommend_bore_diameter(block, cavity_depth_mm)

    if bore_diameter_mm < BORE_DIAMETER_MIN_MM:
        warnings.append(
            f"bore_diameter_mm {bore_diameter_mm:.1f} mm below recommended minimum "
            f"{BORE_DIAMETER_MIN_MM} mm. Smaller bores require higher flow rates for turbulence."
        )
    if bore_diameter_mm > BORE_DIAMETER_MAX_MM:
        warnings.append(
            f"bore_diameter_mm {bore_diameter_mm:.1f} mm above recommended maximum "
            f"{BORE_DIAMETER_MAX_MM} mm. Large bores reduce temperature uniformity."
        )

    # Warn if cavity depth is close to block height
    if cavity_depth_mm >= block.height_mm * 0.8:
        warnings.append(
            f"cavity_depth_mm ({cavity_depth_mm:.1f}) is {cavity_depth_mm/block.height_mm*100:.0f}% "
            f"of block height ({block.height_mm:.1f} mm). Very little room for cooling channels."
        )

    # Generate layout
    if layout_type == "conventional":
        channels, layout_warnings = _design_conventional_layout(
            block=block,
            bore_diameter_mm=bore_diameter_mm,
            cavity_depth_mm=cavity_depth_mm,
            pitch_factor=pitch_factor,
            clearance_factor=clearance_factor,
            num_circuits=num_circuits,
        )
        pitch_mm = pitch_factor * bore_diameter_mm
    else:
        channels, layout_warnings = _design_conformal_layout(
            block=block,
            bore_diameter_mm=bore_diameter_mm,
            cavity_depth_mm=cavity_depth_mm,
            clearance_factor=clearance_factor,
            num_segments_per_loop=num_conformal_segments,
        )
        pitch_mm = CONFORMAL_OFFSET_FACTOR * bore_diameter_mm  # notional pitch for conformal

    warnings.extend(layout_warnings)

    # Compute summary statistics
    total_length = sum(ch.length_mm for ch in channels)
    heat_area = math.pi * (bore_diameter_mm * 1e-3) * (total_length * 1e-3) * 1e6  # mm²
    # (π × D[m] × L[m] → m², × 1e6 → mm²)
    heat_area = math.pi * bore_diameter_mm * total_length  # mm²  (D in mm, L in mm)

    clearance_mm = clearance_factor * bore_diameter_mm
    num_actual_circuits = len(set(ch.circuit_id for ch in channels)) if channels else 0

    caveat = (
        f"HONEST: Channel positions are heuristic estimates from Menges 2001 §6.5 rules. "
        f"{'Straight gun-drilled channels; verify drill access faces.' if layout_type == 'conventional' else 'Conformal loop is a rectangular-perimeter approximation; use CAD-kernel offset for free-form cavities.'} "
        f"Verify interference via kerf_mold.cooling_channel_conflict; "
        f"verify thermal performance via kerf_mold.cooling (Dittus-Boelter). "
        f"Actual cooling optimisation requires Moldflow / Moldex3D FEM simulation. "
        f"Ref: Menges 2001 §6.5; Tang et al. (1998) FEA 26(3) 229–251; "
        f"Xu et al. (2001) PES 41(7) 1265–1279."
    )

    return CoolingLayoutReport(
        channels=channels,
        layout_type=layout_type,
        total_channels=len(channels),
        total_length_mm=round(total_length, 2),
        estimated_heat_area_mm2=round(heat_area, 2),
        pitch_mm=round(pitch_mm, 3),
        bore_diameter_mm=round(bore_diameter_mm, 3),
        clearance_mm=round(clearance_mm, 3),
        num_circuits=num_actual_circuits,
        warnings=warnings,
        honest_caveat=caveat,
    )
